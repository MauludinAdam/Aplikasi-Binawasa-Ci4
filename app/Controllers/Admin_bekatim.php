{
    $bekatim =$this->ModelPelatih_bekatim->find($idpelatih_bekatim);

    // Hapus gambar
    unlink('image/' . $bekatim['foto']);

    $data = [
        'idpelatih_bekatim' => $idpelatih_bekatim,
    ];

    $this->ModelPelatih_bekatim->deleteData_pelatih($idpelatih_bekatim, $data);

